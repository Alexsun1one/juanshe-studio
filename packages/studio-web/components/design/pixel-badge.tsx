export function PixelBadge({
  kind,
  size = 32,
  className,
  ariaLabel,
}: {
  kind: PixelBadgeKind
  size?: number
  className?: string
  ariaLabel?: string
}) {
  const spec = PIXELS[kind]
  const colorOf = (ch: string): string | null => {
    if (ch === ".") return null
    if (ch === "#") return INK
    if (ch === "S") return SKIN
    if (ch === "0") return spec.palette["0"]
    if (ch === "1") return spec.palette["1"]
    if (ch === "2") return spec.palette["2"] ?? spec.palette["0"]
    return null
  }
  return (
    <svg
      viewBox="0 0 16 16"
      width={size}
      height={size}
      className={className}
      role={ariaLabel ? "img" : "presentation"}
      aria-label={ariaLabel}
      style={{ shapeRendering: "crispEdges", imageRendering: "pixelated", display: "block" }}
    >
      {spec.grid.map((row, y) =>
        Array.from(row).map((ch, x) => {
          const c = colorOf(ch)
          if (!c) return null
          return <rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={c} />
        }),
      )}
    </svg>
  )
}
